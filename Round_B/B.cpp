LL a[1010];
int n;
LL d;

inline void work() {
    cin >> n >> d;
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    LL ans = d;
    for (int i = n - 1; i >= 0; --i) {
        ans = ans / a[i] * a[i];
    }
    cout << ans << "\n";
}

int main() {
    // freopen("B.txt", "r", stdin);
    quickread();
    int T;
    cin >> T;
    for (int _ = 1; _ <= T; ++_) {
        cout << "Case #" << _ << ": ";
        work();
    }
    return 0;
}